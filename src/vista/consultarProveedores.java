    public void conectarBase(){
        try{ //inicia try
                Class.forName("com.mysql.jdbc.Driver");
                cn=DriverManager.getConnection("jdbc:mysql://localhost/bdproyectofinalis","root","");
                JOptionPane.showMessageDialog(null, "Conexion a BD OK\n\nLuna");
                stmt=cn.createStatement();//genera sentencias sobre objetos de base de datos
        }catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error de base de datos 1: \n"+ ex);
        }catch(Exception e){
                JOptionPane.showMessageDialog(null,"Error de base de datos 2:"+ e);
        }
    }
    
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try{
            conectarBase();
            
            //Declaracion de un onjeto de almacenamiento l√≥gico de datos "Modelo de datos"
            DefaultTableModel modeloDatos = new DefaultTableModel();
            jtConsulta.setModel(modeloDatos);
            rs = stmt.executeQuery("SELECT * FROM tproveedores");
            
            ResultSetMetaData rsmd = rs.getMetaData();
            int cantidadColumnas = rsmd.getColumnCount();
            
            modeloDatos.addColumn("Id");
            modeloDatos.addColumn("Titulo");
            modeloDatos.addColumn("Nombre");
            modeloDatos.addColumn("Apellido Paterno");
            modeloDatos.addColumn("Apellido Materno");
            modeloDatos.addColumn("Empresa");
            modeloDatos.addColumn("Telefono");
            modeloDatos.addColumn("Email");
            
            //imprimir filas o registros
            while(rs.next()){
                Object fila [] = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i] = rs.getObject(i+1);
                }
                
                modeloDatos.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error 2 de BD Consulta"+e);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

